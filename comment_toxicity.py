def predict(text):
    model.eval()
    with torch.no_grad():
        x = torch.tensor([tokenize(text)], dtype=torch.long).to(device)
        scores = model(x)[0].cpu().numpy()
    print(f"\nComment: \"{text}\"")
    print("-" * 50)
    for name, score in zip(LABEL_NAMES, scores):
        bar = '█' * int(score * 30)
        print(f"  {name:<15} {score:.2%}  {bar}")

predict("Thanks for the great explanation, really helpful!")
